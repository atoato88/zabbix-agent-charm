
Overview
--------

this charm install zabbix-agent, but not create relationship with zabbix-server.
for creating relationship with zabbix-server, you set zabbix-server-ip-address value in config.yaml file.
so, this charm assume zabbix-server has created already.


Usage
-----

Step by step instructions on using the charm:

    juju deploy servicename


Configuration
-------------

See config.yaml file.

